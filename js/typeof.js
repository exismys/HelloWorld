var a;
console.log(typeof a); // undefined

a = "Hello World";
console.log(typeof a); // string

a = 25;
console.log(typeof a); // number

a = 20.5;
console.log(typeof a); // number

a = undefined;
console.log(typeof a); // undefined

a = null;
console.log(typeof a); // object

a = true;
console.log(typeof a); // boolean
